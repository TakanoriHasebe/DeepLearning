from collections import OrderedDict

# 普通のPythonの辞書
temp_dict = {}
temp_dict['A'] = 1
temp_dict['B'] = 2
print(temp_dict)

# 追加した順に辞書を作成する
temp_dict_ordered = OrderedDict()
temp_dict_ordered['A'] = 1
temp_dict_ordered['B'] = 2
print(temp_dict_ordered)                 








